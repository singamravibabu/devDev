# WORKING WITH DICTIONARIES
# Unlike a list, which is ordered collection of items.
# A dictionary is an unordered collection of items.
# A dictionary is a collection of key-value pairs.
# Syntax: {key1:value1, key2:value2, key3:value3, ...}
# The key can be any immutable data type, such as a string, number, or tuple.
# The value can be any data type.

# Creating a dictionary of countries and their codes
country_codes = {"IN":'India',
                 "US":'United States', 
                 "UK":'United Kingdom', 
                 "AU":'Australia'}

print(country_codes)