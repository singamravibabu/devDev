# WORKING WITH STRINGS
# Python uses unicode to sotre the characters in strings
# Strings are immutable

# convert a character to unicode
print(ord('A')) # 65
print(ord('a')) # 97
print(ord('1')) # 49
print(ord(' ')) # 32
print(ord('!')) # 33

# convert unicode to character
print(chr(65)) # A
print(chr(177)) # ±
print(chr(160)) #  non-breaking space
print(chr(169)) # ©
print(chr(180)) # ´
print(chr(190)) # ¾
