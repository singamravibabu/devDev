# type(value) - returns the type/class of the value

print(type(123))        # class: int
print(type(12.3))       # class: float
print(type("123"))      # class: str
print(type(True))       # class: bool

a = 10
b = 2.0
c = "hello world"
d = False

print("What is a?", type(a))
print("What is b?", type(b))
print("What is c?", type(c))
print("What is d?", type(d))
