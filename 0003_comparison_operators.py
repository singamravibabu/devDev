# less than
print(25 < 35)
print(35 < 25)
print("------")

# equal to
print(100 == 100)
print(100 == 200)
print("------")

# not equal to
print(100 != 200)
print(100 != 100)
print("------")

# greater than or equal to
print(100 >= 99)
print(99 >= 100)
print("------")

# less than or equal to
print(100 <= 200)
print(200 <= 100)