# to get the length (number of items) of a list
# len() function is used to find the length of a list
# Syntax: len(list)
# This function can be used to find the length of a list, tuple, dictionary, string, etc.

# Example 1: Find the length of a list
stationary = ['pen', 'pencil', 'eraser', 'sharpener', 'scale']
num_items = len(stationary)
print(num_items)

# Example 2: Find the length of a list
fruits = ['apple', 'banana', 'cherry', 'dates', 'grapes', 'kiwi']
print(len(fruits))