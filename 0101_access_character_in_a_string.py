# access character in a string
# string is a sequence of characters
# each character in a string has an index
# index starts from 0
# index of the last character is -1
# index of the second last character is -2

# access character in a string
str1 = "Hello, World!"
print(str1[0]) # H
print(str1[1]) # e
print(str1[2]) # l

print(str1[-1]) # !
print(str1[-2]) # d
print(str1[-3]) # l